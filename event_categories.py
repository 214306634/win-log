def get_category_by_event_id(event_id, log_type="System"):
    for category_name, category_info in EVENT_CATEGORIES.items():
        if log_type in category_info["log_types"]:
            if event_id in category_info["event_ids"]:
                return category_name
    return "📌 其他事件"

def get_all_event_ids():
    all_ids = set()
    for info in EVENT_CATEGORIES.values():
        all_ids.update(info["event_ids"])
    return sorted(all_ids)

def get_category_names():
    return list(EVENT_CATEGORIES.keys())

def get_event_description(event_id):
    """获取事件ID的中文说明，若没有则返回空字符串"""
    return EVENT_DESCRIPTIONS.get(event_id, "")
